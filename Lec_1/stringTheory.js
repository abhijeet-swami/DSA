// Strings in JavaScript
// A String in JavaScript is a sequence of characters used to represent text.

// Key Characteristics of Strings:
// Immutable: Once a string is created, its content cannot be changed. Any operation that appears to modify a string actually creates a new string.

// Ordered Sequence: Characters are stored in a specific order and can be accessed by their numerical index (starting from 0).

// Primitive Value: Strings are primitive data types.

// Most Used String Methods:
// 1. length
// 2. charAt(index)
// 3. charCodeAt(index)
// 4. slice(start, end)
// 5. substring(indexStart, indexEnd) indexEnd not included in result
// 6. indexOf()
// 7. lastIndexOf()
// 8. includes()
// 9. startsWith()
// 10. endsWith()
// 11. split()
// 12. join()
// 13. toLowerCase()
// 14. trim()
